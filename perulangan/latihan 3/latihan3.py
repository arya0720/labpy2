# Program mencetak pola angka dengan perulangan bertingkat

for i in range(10):           # baris (0 sampai 9)
    for j in range(10):       # kolom (0 sampai 9)
        print(i + j, end="\t")  # tampilkan hasil penjumlahan i+j
    print()  # ganti baris baru setelah 1 baris selesai
# program selesai 
